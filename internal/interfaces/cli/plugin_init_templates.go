func TestEchoOperationSuccess(t *testing.T) {
	result, err := newPlugin().HandleOperation(context.Background(), "echo", map[string]any{"text": "hello"})

	require.NoError(t, err)
	require.True(t, result.Success)
	assert.Equal(t, "hello", result.Output)
	assert.Equal(t, map[string]any{"output": "hello", "text": "hello", "prefix": ""}, result.Data)
}

func TestEchoOperationPrefix(t *testing.T) {
	result, err := newPlugin().HandleOperation(context.Background(), "echo", map[string]any{
		"text":   "hello",
		"prefix": "say: ",
	})

	require.NoError(t, err)
	require.True(t, result.Success)
	assert.Equal(t, "say: hello", result.Output)
	assert.Equal(t, "say: ", result.Data["prefix"])
}

func TestEchoOperationMissingText(t *testing.T) {
	result, err := newPlugin().HandleOperation(context.Background(), "echo", map[string]any{})

	require.NoError(t, err)
	require.False(t, result.Success)
	assert.Contains(t, result.Error, "text")
}

func TestEchoOperationEmptyText(t *testing.T) {
	result, err := newPlugin().HandleOperation(context.Background(), "echo", map[string]any{"text": ""})

	require.NoError(t, err)
	require.False(t, result.Success)
	assert.Contains(t, result.Error, "text")
}

func TestEchoOperationUnknownOperation(t *testing.T) {
	result, err := newPlugin().HandleOperation(context.Background(), "missing", map[string]any{"text": "hello"})

	require.NoError(t, err)
	require.False(t, result.Success)
	assert.Contains(t, result.Error, "unknown operation")
}

func TestEchoOperationSchemaMetadata(t *testing.T) {
	schema, ok := newPlugin().GetOperationSchema("echo")

	require.True(t, ok)
	assert.Contains(t, schema.Description, "Echo")
	require.Len(t, schema.Inputs, 2)
	assert.Equal(t, "text", schema.Inputs[0].Name)
	assert.True(t, schema.Inputs[0].Required)
	assert.Equal(t, "prefix", schema.Inputs[1].Name)
	assert.False(t, schema.Inputs[1].Required)
	assert.ElementsMatch(t, []string{"output", "text", "prefix"}, []string{
		schema.Outputs[0].Name,
		schema.Outputs[1].Name,
		schema.Outputs[2].Name,
	})
}

