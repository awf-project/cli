const releaseWorkflowTemplate = `name: release

on:
  push:
    tags:
      - "v*"

jobs:
  release:
    runs-on: ubuntu-latest
    strategy:
      matrix:
        include:
          - goos: linux
            goarch: amd64
          - goos: linux
            goarch: arm64
          - goos: darwin
            goarch: amd64
          - goos: darwin
            goarch: arm64
    steps:
      - uses: actions/checkout@v4
      - uses: actions/setup-go@v5
        with:
          go-version-file: go.mod
      - name: Build
        env:
          GOOS: ${{ "{{" }} matrix.goos {{ "}}" }}
          GOARCH: ${{ "{{" }} matrix.goarch {{ "}}" }}
          GOFLAGS: -mod=mod
        run: |
          mkdir -p dist/{{.Release.PackageDir}}
          go build -o "dist/{{.Release.PackageDir}}/{{.Release.BinaryName}}" .
          cp {{.Release.ManifestName}} dist/{{.Release.PackageDir}}/{{.Release.ManifestName}}
          tar -C dist/{{.Release.PackageDir}} -czf "dist/{{.DistributionName}}_{{.Release.Version}}_${GOOS}_${GOARCH}.tar.gz" .
      - name: Checksums
        run: |
          cd dist
          sha256sum *.tar.gz > checksums.txt
      - uses: softprops/action-gh-release@v2
        with:
          files: |
            dist/*.tar.gz
            dist/checksums.txt
`
